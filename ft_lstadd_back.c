#include "libft.h"

void		ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list *temp;

	temp = 0;
	if (!lst || !new)
		return ;
	if (!*lst)
	{
		*lst = new;
		return ;
	}
	temp = ft_lstlast(*lst);
	temp->next = new;
}
