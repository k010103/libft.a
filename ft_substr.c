#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	size_t		l;
	size_t		i;
	char		*str;

	l = ft_strlen(s);
	i = 0;
	if (l <= start || len == 0)
	{
		len = 0;
		start = 0;
	}
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (NULL);
	while (i < len)
	{
		str[i] = s[start + i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
